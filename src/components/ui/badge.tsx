import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border-2 px-2.5 py-0.5 text-xs font-semibold transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2 shadow-md hover:shadow-lg",
  {
    variants: {
      variant: {
        default: "border-gray-400 bg-primary text-primary-foreground hover:bg-primary/80 shadow-primary/30",
        secondary: "border-gray-400 bg-secondary text-secondary-foreground hover:bg-secondary/80 shadow-secondary/30",
        destructive: "border-gray-400 bg-destructive text-destructive-foreground hover:bg-destructive/80 shadow-destructive/30",
        outline: "text-foreground border-gray-400 shadow-gray-400/20",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  },
);

export interface BadgeProps extends React.HTMLAttributes<HTMLDivElement>, VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return <div className={cn(badgeVariants({ variant }), className)} {...props} />;
}

export { Badge, badgeVariants };
